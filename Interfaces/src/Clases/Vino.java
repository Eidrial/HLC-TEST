package Clases;

public class Vino {
	
	//Atributos
	private String marca;
	private String tipoVino;
	private double grados;
	private double precio;
	
	//Constructor
	public Vino(String marca, String tipoVino, double grados, double precio) {
		this.marca = marca;
		this.tipoVino = tipoVino;
		this.grados = grados;
		this.precio = precio;
	}
	
	
}
